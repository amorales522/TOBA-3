
package JavaBeans;


public class Transaction {
    
    private int id;
    private Account acount ;
    private double amount;
    private String type;
    private String time;

    public Transaction(){
    this.acount = new Account();};
    public Transaction(Account acount, double amount, String type) {
        this.acount = acount;
        this.amount = amount;
        this.type = type;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Account getAcount() {
        return acount;
    }

    public void setAcount(Account acount) {
        this.acount = acount;
    }

    public double getAmount() {
        return amount;
    }

    public void setAmount(double amount) {
        this.amount = amount;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
    
    
}
